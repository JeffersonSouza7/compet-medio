# 1 - Calcule a área de um retângulo (base*altura).

base = 15
altura = 30

area = base * altura

print(f'A area de um retangulo de base {base} e altural {altura} e igual a: {area}m2')

# 2 - Converta minutos em horas inteiras e minutos restantes (use // e %).

minutos = 135
hora = 60

print(f'O valor de {minutos} minutos e igual a: {minutos // hora} horas e {minutos % hora} minutos')

# 3 - Dado um valor, calcule o quadrado e o cubo.

valor = 7

valorAoQuadrado = valor * valor
valorAoCubo = valor * valor * valor

print(f'O numero {valor} ao elevado ao quadrado e igual a: {valorAoQuadrado}')
print(f'O numero {valor} ao elevado ao cubo e igual a: {valorAoCubo}')